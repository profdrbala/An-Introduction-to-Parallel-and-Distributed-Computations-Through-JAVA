// this program having different data types with simple use  
//datatypes.java
class Type {
public static void main(String args[]) {
	byte m=17; byte n=17; //declares variables with initializing a values
	int l; 
	l= (m+n);
		System.out.println(l);
	float h;   h=20.607f; // declaring and initiating float value
		System.out.println(h);
	double g;   g=20.1234567; // declaring and initiating double value
		System.out.println(g);
	char a,b;   a='D';   b='6'; // declaring and initiating character value
		System.out.println("character A:"+a);
		System.out.println("character B:"+b);
	String s;   s="dhandapani"; // declaring and initiating string value
		System.out.println(s);
	boolean t,f; // declaring boolean type
	t=true;   f=false;
		System.out.println("T Value:"+t);
		System.out.println("F Value :"+f); 
} 
}	
